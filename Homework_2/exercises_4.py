#6.Comparison
#Input two positive integers, and output a line describing their relation.
#Follow the sample format.

a = int(input("Write the first number! "))
b = int(input("Write the second number! "))

if a > b:
    print(a," > ",b)
if a < b:
    print(a," < ",b)
if a == b:
    print(a," = ",b)        